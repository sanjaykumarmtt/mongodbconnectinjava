package mypr;
public class pogo {
   String name;
   String sub;
   int year;
public pogo(String name, String sub, int year) {
	super();
	this.name = name;
	this.sub = sub;
	this.year = year;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getSub() {
	return sub;
}
public void setSub(String sub) {
	this.sub = sub;
}
public int getYear() {
	return year;
}
public void setYear(int year) {
	this.year = year;
}
	
	



}
